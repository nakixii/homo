def nrps_get_msgid_str(sendpid, rcvpid, msgid):
    msgid = 0x0000FFFF & msgid

    if sendpid == "NRRM":
        sendpid = "NRRC"

    if rcvpid == "NRRM":
        rcvpid = "NRRC"

    if sendpid == "ERMM":
        sendpid = "LRRC"

    if rcvpid == "ERMM":
        rcvpid = "LRRC"

    str_msg_merge = '%s_%s_0x%x' % (sendpid, rcvpid, msgid)
    str_msg_merge_d = '%s_%s_%d' % (sendpid, rcvpid, msgid)
    for msgidindex in NRRC_ALL_MSG_ENUM_TABLE.keys():
        if msgidindex.upper() == str_msg_merge.upper() or \
           msgidindex.upper() == str_msg_merge_d.upper():
            return NRRC_ALL_MSG_ENUM_TABLE[msgidindex]

    return "UnknowId"


def nrps_get_pid_str(pid):

    for pidindex in NRRC_ALL_PID_ENUM_TABLE.keys():
        if pidindex == pid:
            return NRRC_ALL_PID_ENUM_TABLE[pidindex]

    return "unknownPid"
